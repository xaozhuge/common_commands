# 1. curl -o 是 curl 下载文件时指定保存文件名/路径的核心参数
# 作用: 将远程文件下载到本地, 并自定义保存名称

# 2. -o: 小写字母 o, 是 output (输出) 的缩写, 必须小写
# 本地自定义文件名: 保存的文件名(可带路径)
# 远程文件 URL: 要下载的文件的网络地址
curl -o [本地自定义文件名] [远程文件URL]

# 3. 自定义文件名: 不会使用远程文件的原始名称, 完全由你指定
# 覆盖不提示: 如果本地已存在同名文件, 会直接覆盖, 无警告
# 静默下载: 默认会显示下载进度条, 不想显示可加 -s 参数

# 4. 基础下载(指定文件名)
# 下载百度首页, 保存为 baidu.html
curl -o baidu.html https://www.baidu.com

