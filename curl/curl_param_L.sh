# 1. curl 中的 -L / --location 是跟随重定向核心参数
# 作用: 让 curl 自动响应服务器返回的 3xx 重定向, 跳转到最终目标地址

# 2. 不加 -L: curl 只会返回重定向响应报文, 不会跳转, 拿不到最终内容

# 3. 基础跟随重定向(最常用)
# 示例: github 会自动重定向到 HTTPS
curl -L http://github.com

# 4. 查看重定向路径(不下载内容)
# 配合 -I 只看响应头, 清晰看到跳转过程
curl -L -I http://baidu.com

# 5. 下载文件(自动跟随重定向)
# 很多下载链接会重定向到真实文件地址, 必须加 -L 才能下载成功
# 下载 Node.js 安装包(链接会重定向)
# -O: 自动使用远程文件名保存
# -L: 处理下载链接的重定向
curl -L -O https://nodejs.org/dist/v20.0.0/node-v20.0.0-linux-x64.tar.xz

# 6. 限制最大重定向次数（防止死循环）
# 默认最多跟随 50 次重定向，可手动修改：
# 最多跟随 10 次重定向
curl -L --max-redirs 10 https://example.com

