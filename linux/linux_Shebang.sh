# 1. Shebang 行是脚本文件的第一行，用于指定该脚本使用的解释器。

# 2. Shebang 行以 #! 开头，后跟解释器的绝对路径。

# 3. Shebang 行告诉操作系统在执行该脚本时应该使用哪个解释器

# 4. 使用 POSIX 标准 Shell（通常是 /bin/sh）
#!/bin/sh

# 5. 使用 Bash Shell
#!/bin/bash

# 6. 使用 Python 解释器
#!/usr/bin/env python3

# 7. 在许多 Unix 和类 Unix 系统中，/bin/sh 实际上是一个符号链接，指向系统的默认 Shell
/bin/sh -> bash
