# 1. -g 表示 全局安装, 意味着安装的工具将在系统范围内可用, 而不仅限于当前项目
npm install -g n --registry=https://registry.npmmirror.com

# 2. n 表示 安装的包的名称
# n 是一个 Node.js 版本管理器, 可以安装和切换不同版本的 Node.js
npm install -g n --registry=https://registry.npmmirror.com

# 3. --registry 指定使用的 npm 镜像源
npm install -g n --registry=https://registry.npmmirror.com

